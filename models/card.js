const mongoose = require('mongoose');

// eslint-disable-next-line
const linkRegexp = /^(https?)(\:\/\/)(www\.)?([\w.\-~:\/?#\[\]@!\$&'\(\)\*+,;=%]*)/;
const cardSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
    minlength: 2,
    maxlength: 30,
  },
  link: {
    type: String,
    required: true,
    match: [linkRegexp],
  },
  owner: {
    type: mongoose.Schema.Types.ObjectId,
    ref: 'user',
    required: true,
  },
  likes: [
    {
      type: mongoose.Schema.Types.ObjectId,
      default: [],
    },
  ],
  createdAt: {
    type: Date,
    default: Date.now,
  },
});

module.exports = mongoose.model('card', cardSchema);
